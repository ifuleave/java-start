package Loop.Ex;

public class LoopEx_ForZz {
    public static void main(String[] args) {
        for(int num = 2; num <=20 ; num = num +2){
            System.out.println(num);
        }
        System.out.println();

        for(int num1  = 2, count = 1; count <= 10 ; num1+=2, count++){
            System.out.println(num1);
        }
    }

}
