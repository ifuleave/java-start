package Loop.Ex;

public class LoopEx_GuGuDan {
    public static void main(String[] args) {

        for (int i = 1 ; i < 10; i++){
            System.out.println("i="+i);
            for (int j = 1; j < 10; j++){
                System.out.println(i+"*"+j+"="+i*j);
            }
        System.out.println();
        }
    }
}
